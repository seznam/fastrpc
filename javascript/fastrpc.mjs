function _getDouble() {
    let bytes = [];
    let index = 8;
    while (index--) {
        bytes[index] = _getByte();
    }
    let sign = (bytes[0] & 0x80 ? 1 : 0);
    let exponent = (bytes[0] & 127) << 4;
    exponent += bytes[1] >> 4;
    if (exponent === 0) {
        return Math.pow(-1, sign) * 0;
    }
    let mantissa = 0;
    let byteIndex = 1;
    let bitIndex = 3;
    index = 1;
    do {
        let bitValue = (bytes[byteIndex] & (1 << bitIndex) ? 1 : 0);
        mantissa += bitValue * Math.pow(2, -index);
        index++;
        bitIndex--;
        if (bitIndex < 0) {
            bitIndex = 7;
            byteIndex++;
        }
    } while (byteIndex < bytes.length);
    if (exponent == 0x7ff) {
        if (mantissa) {
            return NaN;
        }
        else {
            return Math.pow(-1, sign) * Infinity;
        }
    }
    exponent -= (1 << 10) - 1;
    return Math.pow(-1, sign) * Math.pow(2, exponent) * (1 + mantissa);
}
function _serializeValue(result, value) {
    if (value === null) {
        result.push(TYPE_NULL << 3);
        return;
    }
    switch (typeof (value)) {
        case "string":
            let strData = te.encode(value);
            let intData = _encodeInt(strData.length);
            let first = TYPE_STRING << 3;
            first += (intData.length - 1);
            result.push(first);
            _append(result, intData);
            _append(result, strData);
            break;
        case "number":
            if (_getHint() == "float") {
                _serializeDouble(result, value);
            }
            else { // int
                if (_version == 3) {
                    let data = _encodeZigzag(value);
                    let first = (TYPE_INT << 3) + (data.length - 1);
                    result.push(first);
                    _append(result, data);
                }
                else {
                    let first = (value >= 0 ? TYPE_INT8P : TYPE_INT8N);
                    first = first << 3;
                    let data = _encodeInt(Math.abs(value));
                    first += (data.length - 1);
                    result.push(first);
                    _append(result, data);
                }
            }
            break;
        case "boolean":
            let data = TYPE_BOOL << 3;
            if (value) {
                data += 1;
            }
            result.push(data);
            break;
        case "object":
            if (value instanceof ArrayBuffer) {
                _serializeArrayBuffer(result, value);
            }
            else if (value instanceof Date) {
                _serializeDate(result, value);
            }
            else if (value instanceof Array) {
                if (_getHint() == "binary") {
                    _serializeBinary(result, value);
                }
                else {
                    _serializeArray(result, value);
                }
            }
            else if (isBinary(value)) {
                _serializeBinary(result, value.value);
            }
            else if (isDouble(value)) {
                _serializeDouble(result, value.value);
            }
            else {
                _serializeStruct(result, value);
            }
            break;
        default: // undefined, function, ...
            throw new Error(`FRPC does not allow value ${value}`);
            break;
    }
}
